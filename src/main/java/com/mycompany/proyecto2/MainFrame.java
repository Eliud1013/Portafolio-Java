    private void pt1ResultButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_pt1ResultButtonActionPerformed

        String opt = pt1ComboBox.getSelectedItem().toString();
        if(opt == "Triángulo"){

            if(pt1CheckBox.isSelected()){
                int baseValue = Integer.parseInt(pt1BaseTextField.getText());
                int alturaValue = Integer.parseInt(pt1AlturaTextField.getText());
                JOptionPane.showMessageDialog(null, "El area del triangulo es: " + (baseValue*alturaValue)/2, "Base del triangulo",  JOptionPane.INFORMATION_MESSAGE);
            }else {
                int baseValue = Integer.parseInt(pt1BaseTextField.getText());
                int alturaValue = Integer.parseInt(pt1AlturaTextField.getText());
                JOptionPane.showMessageDialog(null, "El area del triangulo es: "+(int)(baseValue*alturaValue)/2, "Base del triangulo",  JOptionPane.INFORMATION_MESSAGE);
            }
        }else if(opt == "Círculo"){

            if(pt1CheckBox.isSelected()){
                int radioValue = Integer.parseInt(pt1RadioTextField.getText());
                double result = (Math.PI* (radioValue * radioValue));
                JOptionPane.showMessageDialog(null, "El area del circulo es: " + result, "Radio del circulo", JOptionPane.INFORMATION_MESSAGE);
            }else {
                int radioValue = Integer.parseInt(pt1RadioTextField.getText());
                double result = (Math.PI* (radioValue * radioValue));
                JOptionPane.showMessageDialog(null, "El area del circulo es: "+(int)result, "Radio del circulo", JOptionPane.INFORMATION_MESSAGE);
            }
        }

    }//GEN-LAST:event_pt1ResultButtonActionPerformed

